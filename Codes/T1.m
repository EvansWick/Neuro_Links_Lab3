% 1. Підготовка навчальної вибірки (XOR)
% Вхідні дані (стовпці - це окремі образи)
X = [0 1 0 1; 
     0 0 1 1]; 
% Цільові значення
T = [0 1 1 0]; 

% 2. Створення багатошарової мережі
% Прихований шар із 3 нейронами, функція навчання - 'trainlm'
net = feedforwardnet(3, 'trainlm');

% Налаштування параметрів навчання
net.trainParam.epochs = 1000; % Максимальна кількість епох
net.trainParam.goal = 1e-5;    % Цільова похибка
net.divideFcn = '';           % Використовувати всі дані для навчання

% 3. Тренування моделі
net = train(net, X, T);

% 4. Тестування мережі
test = [0 0 1 1;
        1 0 0 1];
Y = net(test); 
disp('Результати роботи мережі (XOR):');
disp(Y);
disp('Округлені результати:');
disp(round(Y));